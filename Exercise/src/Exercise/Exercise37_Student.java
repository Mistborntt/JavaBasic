package Exercise;

public class Exercise37_Student {
    private String name;
    private int age;

    // 空参构造方法
    public Exercise37_Student() {

    }

    public Exercise37_Student(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}